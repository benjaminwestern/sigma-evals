package sigmaevals_test

import (
	"strings"
	"testing"

	sigmaevals "github.com/benjaminwestern/sigma-evals"
)

func TestResolveRubricSupportsAliasesAndCustomPrompts(t *testing.T) {
	t.Parallel()

	resolution := sigmaevals.ResolveRubric("rubric-accuracy")
	if !resolution.Found || resolution.Rubric.ID != "accuracy" {
		t.Fatalf("resolution = %+v, want accuracy alias", resolution)
	}
	if !strings.Contains(resolution.Prompt, "factual accuracy") {
		t.Fatalf("prompt = %q, want factual accuracy rubric", resolution.Prompt)
	}

	custom := "This is a custom grading rubric."
	resolution = sigmaevals.ResolveRubric(custom)
	if resolution.Found || resolution.Prompt != custom {
		t.Fatalf("resolution = %+v, want custom prompt passthrough", resolution)
	}
}

func TestDefaultRubricRegistryListIsCopied(t *testing.T) {
	t.Parallel()

	rubrics := sigmaevals.DefaultRubricRegistry.List()
	if len(rubrics) == 0 {
		t.Fatal("DefaultRubricRegistry.List() returned no rubrics")
	}
	rubrics[0].Prompt = "mutated"
	if sigmaevals.GetRubricPrompt(rubrics[0].ID) == "mutated" {
		t.Fatal("DefaultRubricRegistry.List() returned mutable registry state")
	}
}

func TestFormatInlineEvalPrompt(t *testing.T) {
	t.Parallel()

	formatted := sigmaevals.FormatInlineEvalPrompt("accuracy", "Input text", "Target text", "Ground truth")
	for _, want := range []string{
		"factual accuracy",
		"Input Context:\nInput text",
		"Expected Ground Truth:\nGround truth",
		"Target Output Generated by Model:\nTarget text",
	} {
		if !strings.Contains(formatted, want) {
			t.Fatalf("formatted prompt missing %q:\n%s", want, formatted)
		}
	}
}

func TestFormatInlineEvalPromptOmitsEmptyGroundTruth(t *testing.T) {
	t.Parallel()

	formatted := sigmaevals.FormatInlineEvalPrompt("rubric", "input", "target", "")
	if strings.Contains(formatted, "Expected Ground Truth:") {
		t.Fatalf("formatted prompt should omit empty ground truth:\n%s", formatted)
	}
}
